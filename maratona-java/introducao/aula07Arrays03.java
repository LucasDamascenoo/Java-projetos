public class aula07Arrays03 {
    public static void main(String[] args) {
        // iterando arrays

        int [] idades = new int[3]; // criando a estrutura do array
        idades[0] = 26;             // passando os valores para cada indice
        idades[1]= 23;
        idades[2]=20;

        for (int i=0;i<3;i++){
            System.out.println(idades[i]);
        }
    }


}
